package ObjectOrientedProgPart2;

public class CostructorSuperInheritanceA {
	
	
	
	public CostructorSuperInheritanceA(){
		System.out.println("parent class");
	}
	
	public CostructorSuperInheritanceA(int i){
		System.out.println("parent value i "+i);
	}
	
	public CostructorSuperInheritanceA(int i, int j){
		System.out.println("parent value i "+i);
		System.out.println("parent value j "+j);
	}

}
